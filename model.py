# Lets Use Sarima
@st.cache_resource
def train_sarima_model(data, order, seasonal_order):
    model = sm.tsa.statespace.SARIMAX(
        data, order=order, seasonal_order=seasonal_order
    )
    return model.fit()

# Use the function
model = train_sarima_model(df['Close'], order=(p, d, q), seasonal_order=(p, d, q, 12))

# Show forecast plot
